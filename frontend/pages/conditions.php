<h2>1. Objet</h2>
<p>
  La plateforme <strong>SolidariSkills</strong> a pour mission de faciliter les échanges de compétences entre particuliers, dans un esprit d'entraide, de collaboration et de respect mutuel. Elle permet à chaque membre de proposer ou de participer à des activités enrichissantes dans des domaines variés tels que le jardinage, le bricolage, la programmation, l’aide aux devoirs, la cuisine, et bien d'autres encore.
</p>
<p>
  SolidariSkills ne se limite pas à une simple mise en relation : elle encourage l’inclusion sociale, le développement personnel, le partage de savoirs et l’engagement citoyen. L’utilisateur peut créer un profil, recevoir des badges pour valoriser ses participations, dialoguer avec d'autres membres via une messagerie intégrée, laisser ou recevoir des avis après chaque activité, et enrichir son réseau à travers un système de contacts.
</p>
<p>
  Chaque échange repose sur le volontariat et la réciprocité, dans une logique non marchande qui valorise la solidarité locale et l’apprentissage mutuel.
</p>

<h2>2. Définitions</h2>
<ul>
  <li>
    <strong>Utilisateur :</strong> toute personne physique ayant créé un compte sur SolidariSkills. Un utilisateur peut créer ou rejoindre une activité, envoyer et accepter des demandes de contact, échanger via la messagerie, recevoir ou laisser des avis, et obtenir des badges pour ses engagements.
  </li>
  <li>
    <strong>Compétence :</strong> tout savoir-faire, connaissance ou aptitude que l'utilisateur souhaite proposer ou acquérir, qu’elle soit technique, manuelle, créative ou intellectuelle.
  <li>
    <strong>Activité :</strong> rencontre ou initiative convenue entre un ou plusieurs utilisateurs pour échanger des compétences. Chaque activité est planifiée sur la plateforme, peut être évaluée par les participants, et donne lieu à l’attribution d’un badge lorsque l'engagement est complet.
  </li>
</ul>

  <h2>3. Accès au service</h2>
  <p>L'accès à SolidariSkills nécessite une connexion Internet et un équipement compatible. Certaines activités peuvent être reportées (notamment en raison des conditions météorologiques).</p>

  <h2>4. Engagements des utilisateurs</h2>
<ul>
  <li>Adopter en toutes circonstances une attitude respectueuse, solidaire et bienveillante, que ce soit lors de la participation à des activités, dans la messagerie ou sur tout autre espace de la plateforme.</li>
  <li>Respecter les autres membres, leurs opinions, leurs limites, leur temps, ainsi que la diversité des profils et des compétences proposés.</li>
  <li>Ne pas publier ou partager de contenu illicite, diffamatoire, haineux, discriminatoire, violent, sexuellement explicite ou portant atteinte aux droits de tiers (notamment la vie privée, le droit à l’image, les droits d’auteur, etc.).</li>
  <li>Utiliser les fonctionnalités de contact, de messagerie et d'évaluation de manière responsable, sans harcèlement, intimidation, tentative d’arnaque, ni spam.</li>
  <li>Prévenir les autres membres dans les plus brefs délais en cas d’annulation ou d’impossibilité de participer à une activité convenue.</li>
  <li>Ne pas détourner la plateforme de son usage initial (ex. : prospection commerciale, publicité non autorisée, etc.).</li>
  <li>Contribuer activement à maintenir un climat de confiance en signalant tout comportement inapproprié ou tout contenu suspect à l’équipe de modération.</li>
</ul>


